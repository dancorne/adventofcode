# frozen_string_literal: true

class Day6
  attr_reader :map

  def initialize(input)
    @map = parse(input)
    @map_height = @map.size
    @map_width = @map[0].size
    @guard_coords = find_guard
    @guard_direction = :N
    @order_of_directions = %i[N W S E]
  end

  def parse(input)
    input.lines.map { |l| l.chomp.split('') }
  end

  def find_guard
    @map.each.with_index do |row, y|
      return [row.index('^'), y] if row.include?('^')
    end
  end

  def move_target
    case @guard_direction
    when :N
      [@guard_coords[0], @guard_coords[1] - 1]
    when :S
      [@guard_coords[0], @guard_coords[1] + 1]
    when :E
      [@guard_coords[0] - 1, @guard_coords[1]]
    when :W
      [@guard_coords[0] + 1, @guard_coords[1]]
    end
  end

  def turn_guard
    @guard_direction = @order_of_directions[(@order_of_directions.index(@guard_direction) + 1) % @order_of_directions.size]
  end

  def part1
    visited = [@guard_coords]
    while move_target[0].between?(0, @map_width - 1) && move_target[1].between?(0, @map_height - 1)
      if ['.', '^'].include?(@map[move_target[1]][move_target[0]])
        @guard_coords = move_target
        visited << @guard_coords
      else
        turn_guard
      end
    end
    visited.uniq.size
  end
end

if __FILE__ == $PROGRAM_NAME
  input = DATA.read
  puts Day6.new(input).part1
  # puts Day6.new(input).part2
end

